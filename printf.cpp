#include <stdio.h>

int main() {
    int angka = 42;
    float pecahan = 3.14;
    char karakter = 'A';
    char string[] = "Hello, world!";

    printf("Contoh penggunaan printf dalam bahasa C:\n");
    printf("Angka: %d\n", angka);
    printf("Pecahan: %f\n", pecahan);
    printf("Karakter: %c\n", karakter);
    printf("String: %s\n", string);

    return 0;
}
