#include <iostream>

using namespace std;

int main()
{
  int a = 5;
  int b = 10;

  if (a > b)
  {
    cout << "Nilai variabel a lebih besar dari variabel b" << endl;
  }

  if (a < b)
  {
    cout << "Nilai variabel a lebih kecil dari variabel b" << endl;
  }

  if (a == b)
  {
    cout << "Nilai variabel a sama dengan variabel b" << endl;
  }

  return 0;
}